    public void sumupTotalSale() {
        double total = 0;
        int rowcount = gold_table.getRowCount();
        double sum = 0;
        for (int i = 0; i < rowcount; i++) {
            sum = sum + Double.parseDouble(gold_table.getValueAt(i, 7).toString());
        }
        total = Double.valueOf(df2.format(sum));
        lbl_totalSales.setText(Double.toString(total));
    }

    public void showDate() {
        goldQ.setGoldToCombo(cmb_date, "Select Date to search");
    }

    public void search() {
        if (cmb_date.getSelectedIndex() <= 0) {
            allRecords();
            Colors();
            sumupTotalSale();
        } else {
            goldQ.setGoldToTableSale(gold_table, cmb_date);
            sumupTotalSale();
        }

    }

